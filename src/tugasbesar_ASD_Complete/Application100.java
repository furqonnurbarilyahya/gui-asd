        String e = tfOut6.getText();

        if (data.equals("")) {
            JOptionPane.showMessageDialog(null, "Please input your data!", "Error", JOptionPane.ERROR_MESSAGE);

        } else {
            Thread th = new Thread() {
                @Override
                public void run() {
                    try {
                        for (int a = 0; a <= 10; a++) {
                            Thread.sleep(600);
                            if (a == 1) {
                                kolom1();
                            }
                            if (a == 2) {
                                kolom2();
                            }
                            if (a == 3) {
                                kolom3();
                            }
                            if (a == 4) {
                                kolom4();
                            }
                            if (a == 5) {
                                kolom5();
                            }
                            if (a == 6) {
                                kolom6();
                            }
                        }
                    } catch (Exception ex) {
                        System.out.println();
                    }
                }
            };
            th.start();
        }
