    private void btnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchMouseClicked
        // TODO add your handling code here:
        //Memanggil Output Sorted Array Start --> osa (output sorted array)
        int osa1 = Integer.valueOf(tfOutArraySearching1.getText());
        int osa2 = Integer.valueOf(tfOutArraySearching2.getText());
        int osa3 = Integer.valueOf(tfOutArraySearching3.getText());
        int osa4 = Integer.valueOf(tfOutArraySearching4.getText());
        int osa5 = Integer.valueOf(tfOutArraySearching5.getText());
        int osa6 = Integer.valueOf(tfOutArraySearching6.getText());
        int[] data = {osa1, osa2, osa3, osa4, osa5, osa6};
        //Memanggil Output Sorted Array Start

        //Deklarasi, Inisialisasi, Assignment Input Start --> isa (input search array)
        int isa1 = Integer.parseInt(tfInputSearchArray.getText());
        int isa2 = Integer.parseInt(tfInputSearchArray.getText());
        int isa3 = Integer.parseInt(tfInputSearchArray.getText());
        int isa4 = Integer.parseInt(tfInputSearchArray.getText());
        int isa5 = Integer.parseInt(tfInputSearchArray.getText());
        int isa6 = Integer.parseInt(tfInputSearchArray.getText());

        int isa = Integer.parseInt(tfInputSearchArray.getText());
        //Deklarasi, Inisialisasi, Assignment Input End

        int index = -1;

        if (tfInputSearchArray.getText().equals("")) {
            System.out.println("empty");
            JOptionPane.showMessageDialog(null, "Please input your data!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            for (int i = 0; i < data.length; i++) {
                if (data[i] == isa) {
                    index = i;
                }
            }
            if (index == -1) {
                tfOutResultSearching.setText("Data not found");
            } else {
                tfOutResultSearching.setText("Data found at index " + index);
            }

        }
    }//GEN-LAST:event_btnSearchMouseClicked

