    public static void quicksort(int[] ar, int start, int end) {
        int[] array;

        int i = start, j = end;
        if (end - start >= 1) {
            int pivot = ar[end];
            while (i < j) {
                while (ar[i] < pivot && i < end) {
                    i++;
                }
                while (ar[j] >= pivot && j > start) {
                    j--;
                }
                if (i < j) {
                    swap(ar, i, j);
                }
            }
            swap(ar, end, i);

            quicksort(ar, start, i - 1);
            quicksort(ar, i + 1, end);
        } else {
            return;
        }
    }

    public static void quicksortDsc(int[] ar, int start, int end) {
        int[] array;

        int i = start, j = end;
        if (end - start >= 1) {
            int pivot = ar[end];
            while (i < j) {
                while (ar[i] > pivot && i < end) {
                    i++;
                }
                while (ar[j] <= pivot && j > start) {
                    j--;
                }
                if (i < j) {
                    swap(ar, i, j);
                }
            }
            swap(ar, end, i);

            quicksortDsc(ar, start, i - 1);
            quicksortDsc(ar, i + 1, end);
        } else {
            return;
        }
    }

    public static void swap(int[] ar, int index1, int index2) {

        int temp = ar[index1];
        ar[index1] = ar[index2];
        ar[index2] = temp;
    }

